    menuLinks.forEach((link => {
        link.addEventListener("mouseover", (() => {
            menuLinks.forEach((otherLink => {
                if (otherLink !== link) otherLink.classList.add("link-inactive");
            }));
        }));
        link.addEventListener("mouseout", (() => {
            menuLinks.forEach((otherLink => {
                otherLink.classList.remove("link-inactive");
            }));
        }));
    }));
    class Timer {
        constructor(hoursElement, minutesElement, secondsElement) {
            this.hoursElement = hoursElement;
            this.minutesElement = minutesElement;
            this.secondsElement = secondsElement;
            this.hours = parseInt(hoursElement.textContent, 10);
            this.minutes = parseInt(minutesElement.textContent, 10);
            this.seconds = parseInt(secondsElement.textContent, 10);
        }
        updateDisplay() {
            this.hoursElement.textContent = String(this.hours).padStart(2, "0") + ":";
            this.minutesElement.textContent = String(this.minutes).padStart(2, "0") + ":";
            this.secondsElement.textContent = String(this.seconds).padStart(2, "0");
        }
        start() {
            if (this.hours > 0 || this.minutes > 0 || this.seconds > 0) this.interval = setInterval((() => {
                if (this.seconds > 0) this.seconds--; else if (this.minutes > 0) {
                    this.minutes--;
                    this.seconds = 59;
                } else if (this.hours > 0) {
                    this.hours--;
                    this.minutes = 59;
                    this.seconds = 59;
                }
                this.updateDisplay();
                if (this.hours === 0 && this.minutes === 0 && this.seconds === 0) this.stop();
            }), 1e3);
        }
        stop() {
            clearInterval(this.interval);
        }
    }
    const timers = document.querySelectorAll(".timer");
    const timerObjects = [];
    timers.forEach((timerElement => {
        const timeElements = timerElement.querySelectorAll(".timer div");
        const timerObject = new Timer(timeElements[0], timeElements[1], timeElements[2]);
        timerObjects.push(timerObject);
        timerObject.start();
    }));
